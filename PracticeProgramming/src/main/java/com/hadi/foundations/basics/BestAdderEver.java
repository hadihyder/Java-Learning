public class BestAdderEver {
    public static void main(String[] args) {

        int num1, num2, num3;
        int sum;

        num1 = 100;
        num2 = 200;
        num3 = 300;

        sum = num1 + num2 + num3;

        System.out.println(num1 + " + " + num2 + " + " + num3 + " = " + sum);
    }
}
